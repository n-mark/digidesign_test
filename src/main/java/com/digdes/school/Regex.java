package com.digdes.school;

public enum Regex {
    WHERE_SPLIT("(?<=(=\\s{0,20}'(.)|(\\d){0,20}')|(=\\s{0,20}\\d\\s{0,20})||(\\d+\\.\\d+)|(=\\s{0,20}\\w{0,20}))\\s*(?i)where\\s*(?=')"),
    AND_SPLIT("(?<=(=\\s{0,20}'(.)|(\\d){0,20}')|(=\\s{0,20}\\d\\s{0,20})||(\\d+\\.\\d+)|(=\\s{0,20}\\w{0,20}))\\s*(?i)and\\s*(?=')"),
    OR_SPLIT("(?<=(=\\s{0,20}'(.)|(\\d){0,20}')|(=\\s{0,20}\\d\\s{0,20})||(\\d+\\.\\d+)|(=\\s{0,20}\\w{0,20}))\\s*(?i)or\\s*(?=')"),
    COMMA_SPLIT("(?<=(=\\s{0,20}'(.)|(\\d){0,20}')|(=\\s{0,20}\\d\\s{0,20})||(\\d+\\.\\d+)|(=\\s{0,20}\\w{0,20}))\\s*,\\s*(?=')"),
    KEY("'.*'"),
    DOUBLE("(\\d+\\.\\d+)"),
    EQUAL("(?<='.*')\\s{0,20}=\\s{0,20}(?=('.*')|(\\s{0,20}-?\\d|(true)|(false)|(null)))"),
    NOT_EQUAL("(?<='.*')\\s{0,20}!=\\s{0,20}(?=('.*')|(\\s{0,20}-?\\d|(true)|(false)|(null)))"),
    MORE_EQUAL("(?<='.*')\\s{0,20}>=\\s{0,20}(?=(\\s{0,20}-?\\d))"),
    LESS_EQUAL("(?<='.*')\\s{0,20}<=\\s{0,20}(?=(\\s{0,20}-?\\d))"),
    MORE("(?<='.*')\\s{0,20}>\\s{0,20}(?=(\\s{0,20}-?\\d))"),
    LESS("(?<='.*')\\s{0,20}<\\s{0,20}(?=(\\s{0,20}-?\\d))"),
    LIKE("(?<='.*')\\s{0,20}like\\s{0,20}(?=('.*'))"),
    ILIKE("(?<='.*')\\s{0,20}ilike\\s{0,20}(?=('.*'))");

    private final String regex;

    Regex(String regex) {
        this.regex = regex;
    }

    public String getRegex() {
        return regex;
    }
}

